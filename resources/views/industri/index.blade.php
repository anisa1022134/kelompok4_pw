        <p style="text-align: justify;">

            Teknik Industri merupakan cabang ilmu teknik yang berasal dari Amerika Utara.
            Lulusan Teknik Industri dibutuhkan untuk menangani perencanaan dan pengendalian produksi, pengendalian kualitas, dan pengembangan sistem manajemen kualitas.
            Mahasiswa Teknik Industri mempelajari berbagai bidang keilmuan, seperti teknik, ekonomi, pengolahan data, perancangan sistem kerja, dan pengembangan produk.
            Lulusan Teknik Industri dapat bekerja di berbagai industri, seperti jasa keuangan, konsultasi, teknologi informasi, pelayanan masyarakat, pemeliharaan, logistik, dan inventori.
        </p>

        <p style="text-align: justify;">
            Teknik Industri adalah jurusan kuliah yang menggabungkan bidang ilmu teknik dengan ilmu manajemen. Itu sebabnya, jurusan ini sangat cocok untuk lulusan IPA yang senang berbisnis, atau yang punya mimpi untuk bisa memimpin sebuah perusahaan industri yang besar. Bukan hanya mempelajari seluk-beluk mesin manufaktur, jurusan ini juga akan mempelajari perencanaan dan pengendalian produksi pabrik, mulai dari manajemen keuangan, operasional, hingga manajemen sumber daya manusia.
        </p>
        <section id="Visi-Misi" class="mt-5">
          <h3 style="text-align: left;">Visi</h3>
          <p style="text-align: justify;">
              Menjadi program studi yang unggul dalam pengembangan ilmu pengetahuan dan teknologi di bidang teknik industri, menghasilkan lulusan yang kompeten, inovatif, berwawasan global, dan berakhlak mulia serta mampu bersaing di tingkat nasional dan internasional pada tahun 2030.
          </p>

          <h4 style="text-align: left;">Misi</h4>

          <ul style="text-align: justify;">
              <li>Menyelenggarakan Pendidikan Teknik Industri Berkualitas</li>
              <p>
                  Menyediakan pendidikan yang berkualitas di bidang teknik industri, berorientasi pada kebutuhan industri dan perkembangan teknologi, dengan fokus pada pemahaman mendalam tentang manajemen produksi, efisiensi, serta pengembangan soft skills untuk menghadapi tantangan global.
              </p>
              <li>Mendorong Riset dan Inovasi di Bidang Teknik Industri</li>
              <p>
                  Mengembangkan penelitian dan inovasi yang relevan dengan perkembangan teknik industri, guna menghasilkan solusi kreatif dan aplikatif yang dapat diterapkan untuk menyelesaikan permasalahan di lingkungan industri dan masyarakat.
              </p>
              <li>Melaksanakan Pengabdian Masyarakat Berbasis Teknik Industri</li>
              <p>
                  Mengimplementasikan ilmu teknik industri melalui kegiatan pengabdian masyarakat, bertujuan untuk meningkatkan produktivitas, efisiensi, dan literasi teknologi dalam masyarakat, serta memberikan dampak positif bagi kesejahteraan sosial.
              </p>
              <li>Membangun Kemitraan dengan Industri dan Institusi Pendidikan</li>
              <p>
                  Menjalin kerjasama strategis dengan berbagai industri, lembaga pendidikan, dan institusi riset baik di dalam maupun luar negeri untuk meningkatkan kualitas pendidikan, penelitian, dan pengabdian serta membuka jaringan profesional bagi mahasiswa.
              </p>
              <li>Memastikan Tata Kelola Program Studi yang Transparan dan Akuntabel</li>
              <p>
                  Menciptakan tata kelola yang baik, transparan, dan akuntabel untuk meningkatkan efisiensi dalam pengelolaan sumber daya, serta menjamin mutu pendidikan, penelitian, dan pelayanan kepada mahasiswa dan masyarakat.
              </p>
          </ul>
      </section>

