        <p>
            Program Studi Informatika menyediakan pendidikan di bidang fondasi teoritis informasi dan komputasi serta teknik praktis untuk aplikasi mereka dalam sistem komputer
        </p>

        <p>
            Pada era globalisasi sekarang ini, IT (Information Technology) sudah menjadi kebutuhan mutlak bagi setiap organisasi termasuk perusahaan baik di bidang jasa maupun barang dan juga pada organisasi nirlaba. Selain itu, teknologi ini juga merupakan salah satu ilmu yang mampu menyentuh masyarakat secara perorangan seperti misalnya dalam komunikasi, hiburan dan pendidikan. Oleh karena itu, kebutuhan tenaga kerja profesional di bidang Teknik Informatika ini sangat tinggi baik di tingkat nasional maupun internasio-nal. Selain diserap oleh pasar tenaga kerja, seorang lulusan Teknik Informatika juga dapat dengan mudah menjadi seorang entrepreneur dengan keahlian dan kreativitasnya di bidang ilmu Informatika.
        </p>

        <section id="Visi-Misi" class="mt-5">
          <h3>Visi</h3>
          <p>
            Menjadi program studi yang unggul dalam pengembangan ilmu pengetahuan dan teknologi informatika, menghasilkan lulusan yang kompeten, inovatif, dan berakhlak mulia serta mampu bersaing di tingkat nasional dan internasional pada tahun 2030.
          </p>

          <h4>Misi</h4>

        <ul style="text-align: justify;">
          <li>Menyelenggarakan Pendidikan Berkualitas</li>
          <p>
           Menyediakan pendidikan yang berkualitas dan berorientasi pada kebutuhan industri dan perkembangan teknologi, dengan menekankan pada pemahaman mendalam tentang informatika serta pengembangan soft skills yang dibutuhkan untuk bersaing di dunia kerja.
          </p>
          <li>Mengembangkan Riset dan Inovasi</li>
          <p>
            Mendorong penelitian dan inovasi di bidang informatika yang sesuai dengan perkembangan ilmu pengetahuan dan teknologi, serta dapat diterapkan dalam masyarakat untuk memberikan solusi terhadap permasalahan lokal maupun global.
          </p>
          <li>Mengabdikan Ilmu untuk Masyarakat</li>
          <p>
            Melaksanakan pengabdian kepada masyarakat dengan penerapan teknologi informatika, guna meningkatkan kesejahteraan dan literasi digital di kalangan masyarakat serta memberikan dampak positif bagi lingkungan sekitar.
          </p>
          <li>Membangun Kemitraan Strategis</li>
          <p>
            Menjalin kerjasama dengan industri, lembaga pendidikan, serta institusi riset dalam dan luar negeri untuk meningkatkan mutu pendidikan, penelitian, dan pengabdian kepada masyarakat serta memperluas jaringan profesional bagi mahasiswa.
          </p>
          <li>Menyelenggarakan Tata Kelola yang Transparan dan Akuntabel</li>
          <p>
            Menciptakan tata kelola jurusan yang baik, transparan, dan akuntabel untuk mencapai efisiensi dalam pengelolaan sumber daya, serta menjamin mutu pendidikan dan pelayanan yang optimal.
          </p>
        </ul>
        </section>

